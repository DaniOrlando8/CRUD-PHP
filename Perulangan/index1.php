       $a < 5; (Kondisi)
       $a++    (Membuat kondisi sampai false) */
       
       /* Perulangan do-while */
       $b = 0;
       do {
           echo "Hallo Indonesia <br>";
           $b++;
        } while ($b <= 5);
        
        /* $b = 0; (inisialisasi variabel)
           $b < 5; (Kondisi)
           $b++    (Membuat kondisi sampai false) */
